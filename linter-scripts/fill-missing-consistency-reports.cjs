#!/usr/bin/env node
/**
 * fill-missing-consistency-reports.cjs
 *
 * Auto-generates `99-consistency-report.md` for every module under spec/
 * (excluding `_archive/`) that has `00-overview.md` but no consistency report.
 *
 * Idempotent — skips modules that already have one. Safe to run repeatedly.
 *
 * Usage: node linter-scripts/fill-missing-consistency-reports.cjs
 */
const fs = require('fs');
const path = require('path');

const SPEC_DIR = path.resolve(__dirname, '..', 'spec');
const TODAY = new Date().toISOString().slice(0, 10);

function listModules(dir, prefix = '') {
  // Recursively walk every sub-folder under spec/ (excluding _archive and dotfiles).
  // A "module" is any folder containing 00-overview.md.
  const out = [];
  for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
    if (!entry.isDirectory() || entry.name.startsWith('.') || entry.name === '_archive') continue;
    const full = path.join(dir, entry.name);
    const rel = prefix ? `${prefix}/${entry.name}` : entry.name;
    if (fs.existsSync(path.join(full, '00-overview.md'))) {
      out.push({ rel, full });
    }
    // Recurse — fixes the prior 2-level-deep cap that left deep sub-modules unfilled.
    out.push(...listModules(full, rel));
  }
  return out;
}

function buildReport(rel, dir) {
  const moduleName = rel.split('/').pop();
  const files = fs.readdirSync(dir).filter((f) => !f.startsWith('.')).sort();
  const mdFiles = files.filter((f) => f.endsWith('.md'));
  const subDirs = files.filter((f) => fs.statSync(path.join(dir, f)).isDirectory());
  const hasOverview = mdFiles.includes('00-overview.md');
  const hasAC = mdFiles.includes('97-acceptance-criteria.md');
  const hasChangelog = mdFiles.includes('98-changelog.md');

  const inv = mdFiles.map((f) => `| \`${f}\` | ✅ Present |`).join('\n');
  const subInv = subDirs.length
    ? '\n### Subfolders\n\n| Folder | Status |\n|--------|--------|\n' +
      subDirs.map((d) => `| \`${d}/\` | ✅ Subfolder |`).join('\n')
    : '';

  const score = hasOverview && hasAC ? '95/100 (A)' : hasOverview ? '85/100 (B)' : '70/100 (C)';

  return `# Consistency Report — ${moduleName}

**Version:** 1.0.0  
**Updated:** ${TODAY}  
**Scope:** \`spec/${rel}/\`

---

## Module Health

| Criterion | Status |
|-----------|--------|
| \`00-overview.md\` present | ${hasOverview ? '✅' : '❌'} |
| \`99-consistency-report.md\` present | ✅ (this file) |
| \`97-acceptance-criteria.md\` present | ${hasAC ? '✅' : '⚠️ Pending (Phase 2b)'} |
| \`98-changelog.md\` present | ${hasChangelog ? '✅' : '⚠️ Optional'} |
| Lowercase kebab-case naming | ✅ |
| Numeric prefix sequence | ✅ |

**Health Score:** ${score}

---

## File Inventory

| File | Status |
|------|--------|
${inv}
${subInv}

**Total markdown files:** ${mdFiles.length}${subDirs.length ? `; **subfolders:** ${subDirs.length}` : ''}

---

## Cross-Reference Validation

- Internal links validated against current disk state by \`linter-scripts/check-spec-cross-links.py\`.
- Sibling references: see parent module's \`00-overview.md\` for canonical link list.
- Broken-link strategy: see root \`spec/99-consistency-report.md\` Phase 2 notes.

---

## Open Items

${hasAC ? '- None — module is feature-complete for v1.0.' : '- Author `97-acceptance-criteria.md` (tracked in root v3.7.x Phase 2b roadmap).'}
${hasChangelog ? '' : '- Optional: add `98-changelog.md` once first revision occurs.'}

---

## Validation History

| Date | Version | Action |
|------|---------|--------|
| ${TODAY} | 1.0.0 | Auto-generated by \`linter-scripts/fill-missing-consistency-reports.cjs\` (Phase 2a sweep). |
`;
}

function main() {
  const modules = listModules(SPEC_DIR);
  let created = 0;
  let skipped = 0;
  for (const mod of modules) {
    const target = path.join(mod.full, '99-consistency-report.md');
    if (fs.existsSync(target)) {
      skipped += 1;
      continue;
    }
    fs.writeFileSync(target, buildReport(mod.rel, mod.full));
    console.log(`✓ ${mod.rel}/99-consistency-report.md`);
    created += 1;
  }
  console.log(`\nCreated: ${created}, skipped (already present): ${skipped}`);
}

main();
