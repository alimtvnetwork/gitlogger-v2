#!/usr/bin/env node
/**
 * generate-spec-index.cjs
 * Walks `spec/` and rebuilds `spec/spec-index.md` from disk truth.
 * Usage: node linter-scripts/generate-spec-index.cjs
 */
const fs = require('fs');
const path = require('path');
const SPEC_DIR = path.resolve(__dirname, '..', 'spec');
const OUT_FILE = path.join(SPEC_DIR, 'spec-index.md');

const CATEGORY_ICONS = {
  '01-spec-authoring-guide': '📘 Spec Authoring Guide',
  '02-coding-guidelines': '📐 Coding Guidelines',
  '03-error-manage': '🔴 Error Management',
  '04-database-conventions': '🗄️ Database Conventions',
  '05-split-db-architecture': '🗄️ Split DB Architecture',
  '06-seedable-config-architecture': '🌱 Seedable Config',
  '07-design-system': '🎨 Design System',
  '10-research': '🔬 Research',
  '11-powershell-integration': '⚡ PowerShell Integration',
  '12-cicd-pipeline-workflows': '🔄 CI/CD Pipeline Workflows',
  '13-generic-cli': '💻 Generic CLI',
  '14-update': '🔄 Update',
  '15-distribution-and-runner': '📦 Distribution & Runner',
  '16-generic-release': '🚀 Generic Release',
  '17-consolidated-guidelines': '📋 Consolidated Guidelines',
  '18-wp-plugin-how-to': '🔌 WP Plugin How-To',
  '22-git-logs-v2': '📜 Git Logs v2 (App)',
  '23-app-database': '🗄️ App Database',
  '24-app-design-system-and-ui': '🎨 App Design System & UI',
  '25-app-issues': '🐛 App Issues',
  '26-gitlogs-diagrams': '📊 Git Logs Diagrams',
  '_archive': '📦 Archive (deprecated)',
};

function walk(dir, files = []) {
  for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
    const full = path.join(dir, entry.name);
    if (entry.isDirectory()) walk(full, files);
    else if (entry.name.endsWith('.md')) files.push(full);
  }
  return files;
}

function extractMeta(filePath) {
  let title = path.basename(filePath, '.md');
  let version = '';
  try {
    const content = fs.readFileSync(filePath, 'utf8').slice(0, 2048);
    const h1 = content.match(/^#\s+(.+)$/m);
    if (h1) title = h1[1].trim();
    const ver = content.match(/\*\*Version:\*\*\s*([0-9][^\s<\n]*)/);
    if (ver) version = ver[1].trim();
  } catch (_) {}
  return { title, version };
}

function categoryFor(relPath) {
  const parts = relPath.split(path.sep);
  return parts.length === 1 ? '__root__' : parts[0];
}

function slugify(s) {
  return s.toLowerCase().replace(/[^a-z0-9]+/g, '-').replace(/^-|-$/g, '');
}

function main() {
  const allFiles = walk(SPEC_DIR);
  const grouped = new Map();
  for (const abs of allFiles) {
    const rel = path.relative(SPEC_DIR, abs);
    if (rel === 'spec-index.md') continue;
    const cat = categoryFor(rel);
    if (!grouped.has(cat)) grouped.set(cat, []);
    grouped.get(cat).push({ rel, ...extractMeta(abs) });
  }
  for (const list of grouped.values()) list.sort((a, b) => a.rel.localeCompare(b.rel));

  const today = new Date().toISOString().slice(0, 10);
  const orderedCats = [
    '__root__',
    ...Object.keys(CATEGORY_ICONS).filter((k) => grouped.has(k)),
    ...[...grouped.keys()].filter((k) => k !== '__root__' && !(k in CATEGORY_ICONS)),
  ];

  const lines = [
    '# Spec File Index — Searchable Reference',
    '',
    `**Generated:** ${today}  `,
    `**Total Files:** ${allFiles.length}  `,
    `**Categories:** ${grouped.size}`,
    '',
    '> Auto-generated by `linter-scripts/generate-spec-index.cjs` — do not hand-edit.',
    '> Use `Ctrl+F` / `Cmd+F` to search by keyword, file name, or category.',
    '',
    '---',
    '',
    '## Table of Contents',
    '',
  ];
  for (const cat of orderedCats) {
    if (!grouped.has(cat)) continue;
    const label = cat === '__root__' ? '📄 Root' : CATEGORY_ICONS[cat] || `📁 ${cat}`;
    const count = grouped.get(cat).length;
    lines.push(`- [${label}](#${slugify(label)}) (${count} file${count === 1 ? '' : 's'})`);
  }
  lines.push('', '---', '');
  for (const cat of orderedCats) {
    if (!grouped.has(cat)) continue;
    const label = cat === '__root__' ? '📄 Root' : CATEGORY_ICONS[cat] || `📁 ${cat}`;
    lines.push(`## ${label}`, '', '| File | Title | Version |', '|------|-------|---------|');
    for (const f of grouped.get(cat)) {
      const safeTitle = f.title.replace(/\|/g, '\\|');
      lines.push(`| \`spec/${f.rel.replace(/\\/g, '/')}\` | ${safeTitle} | ${f.version} |`);
    }
    lines.push('');
  }
  fs.writeFileSync(OUT_FILE, lines.join('\n') + '\n');
  console.log(`✓ Wrote ${OUT_FILE} (${allFiles.length} files, ${grouped.size} categories)`);
}
main();
